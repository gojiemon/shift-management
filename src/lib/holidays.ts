/**
 * 日本の祝日判定ユーティリティ
 * 2024〜2027年の祝日をカバー
 */

// 春分の日・秋分の日は年によって変わるため、天文計算に基づく値をハードコード
const FIXED_HOLIDAYS: Record<number, string[]> = {
  2024: [
    "01-01", "01-08", "02-11", "02-12", "02-23",
    "03-20", "04-29", "05-03", "05-04", "05-05", "05-06",
    "07-15", "08-11", "08-12", "09-16", "09-22", "09-23",
    "10-14", "11-03", "11-04", "11-23",
  ],
  2025: [
    "01-01", "01-13", "02-11", "02-23", "02-24",
    "03-20", "04-29", "05-03", "05-04", "05-05", "05-06",
    "07-21", "08-11", "09-15", "09-23",
    "10-13", "11-03", "11-23", "11-24",
  ],
  2026: [
    "01-01", "01-12", "02-11", "02-23",
    "03-20", "04-29", "05-03", "05-04", "05-05", "05-06",
    "07-20", "08-11", "09-21", "09-23",
    "10-12", "11-03", "11-23",
  ],
  2027: [
    "01-01", "01-11", "02-11", "02-23",
    "03-21", "03-22", "04-29", "05-03", "05-04", "05-05",
    "07-19", "08-11", "09-20", "09-23",
    "10-11", "11-03", "11-23",
  ],
};

/**
 * 指定した日付が日本の祝日かどうかを判定
 */
export function isJapaneseHoliday(date: Date): boolean {
  const year = date.getFullYear();
  const mmdd = `${String(date.getMonth() + 1).padStart(2, "0")}-${String(date.getDate()).padStart(2, "0")}`;
  const holidays = FIXED_HOLIDAYS[year];
  if (!holidays) return false;
  return holidays.includes(mmdd);
}

/**
 * 土日または祝日かどうか
 */
export function isWeekendOrHoliday(date: Date): boolean {
  const dow = date.getDay();
  return dow === 0 || dow === 6 || isJapaneseHoliday(date);
}
